DROP DATABASE IF EXISTS onlineshop;
CREATE DATABASE onlineshop;
USE onlineshop;

    CREATE TABLE kunden (
        kundennummer INT AUTO_INCREMENT PRIMARY KEY,
        nachname VARCHAR(255),
        vorname VARCHAR(255),
        strasse VARCHAR(255),
        plz VARCHAR(5),
        ort VARCHAR(100)
    );
    
    CREATE TABLE bestellung (
        bestellnummer INT AUTO_INCREMENT PRIMARY KEY,
        kundennummer INT,
        bestelldatum DATE,
        FOREIGN KEY (kundennummer) REFERENCES kunden(kundennummer)
    );
    
    CREATE TABLE artikelgruppe (
        artikelgruppennummer INT AUTO_INCREMENT PRIMARY KEY,
        bezeichnung VARCHAR(255),
        rabatt VARCHAR(50)
    );
    
    CREATE TABLE artikel (
        artikelnummer INT AUTO_INCREMENT PRIMARY KEY,
        bezeichnung VARCHAR(255),
        preis DECIMAL(10, 2),
        artikelgruppennummer INT,
        FOREIGN KEY (artikelgruppennummer) REFERENCES artikelgruppe(artikelgruppennummer)
    );
    
    CREATE TABLE position (
        bestellnummer INT,
        artikelnummer INT,
        menge INT,
        PRIMARY KEY (bestellnummer, artikelnummer),
        FOREIGN KEY (bestellnummer) REFERENCES bestellung(bestellnummer),
        FOREIGN KEY (artikelnummer) REFERENCES artikel(artikelnummer)
    );
    
